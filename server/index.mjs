// 内容新鲜度检测
function analyzeContentFreshness(html, $) {
  const bodyText = getCleanText($);

  // 优先从meta标签和结构化数据提取日期（更可靠）
  let metaDateScore = 0;
  const hasPublishedMeta = $('meta[property="article:published_time"]').length > 0 ||
                           $('meta[name="date"]').length > 0 ||
                           $('meta[name="datePublished"]').length > 0;
  const hasModifiedMeta = $('meta[property="article:modified_time"]').length > 0 ||
                          $('meta[name="last-modified"]').length > 0;
  if (hasPublishedMeta) metaDateScore += 50;
  if (hasModifiedMeta) metaDateScore += 30;

  // 从JSON-LD提取日期
  let schemaDateFound = false;
  $('script[type="application/ld+json"]').each((i, el) => {
    try {
      const parsed = JSON.parse($(el).html());
      const schemas = Array.isArray(parsed) ? parsed : [parsed];
      schemas.forEach(s => {
        if (s.datePublished || s.dateModified) schemaDateFound = true;
        if (s['@graph']) s['@graph'].forEach(g => { if (g.datePublished || g.dateModified) schemaDateFound = true; });
      });
    } catch (e) {}
  });
  if (schemaDateFound) metaDateScore += 20;

  // 检测时间标记 - 多格式（中文、英文、ISO格式）
  const datePatterns = /(\d{4})年|(\d{1,2})月|最近|近期|今年|去年|本周|本月|最新|last updated|published on|updated on|posted on|\d{4}-\d{2}-\d{2}|\d{2}\/\d{2}\/\d{4}|Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec/gi;
  const dateCount = (bodyText.match(datePatterns) || []).length + (metaDateScore > 0 ? 3 : 0);

  // 检测版本号或更新标记 - 多格式
  const versionPatterns = /v\d+\.\d+|版本|更新|升级|修订|changelog|release|what'?s new|更新日志|release notes/gi;
  const versionCount = (bodyText.match(versionPatterns) || []).length;

  // 检测时效性关键词 - 使用动态年份
  const currentYear = new Date().getFullYear();
  const recentYears = [currentYear, currentYear - 1].join('|');
  const freshnessKeywords = new RegExp(`${recentYears}|当前|目前|现在|最新|最近发布|latest|current|recently|as of`, 'gi');
  const freshnessCount = (bodyText.match(freshnessKeywords) || []).length;

  // 检测过时内容标记 - 多格式
  const outdatedPatterns = /以前|过去|曾经|旧版|过时|不再适用|deprecated|outdated|legacy|archived|obsolete|former/gi;
  const outdatedCount = (bodyText.match(outdatedPatterns) || []).length;

  const freshnessScore = {
    hasDateMarkers: dateCount > 0 || metaDateScore > 0,
    dateCount,
    hasVersionInfo: versionCount > 0,
    versionCount,
    metaDateScore,
    freshnessScore: freshnessCount,
    outdatedScore: outdatedCount,
    overallFreshness: 0,
    recommendation: ''
  };

  // 计算新鲜度评分 - 使用加权算法
  const freshnessWeights = {
    metaDateScore: 20,      // meta/结构化数据日期 - 高权重（最可靠）
    dateCount: 25,          // 时间标记 - 高权重
    versionCount: 20,       // 版本信息 - 中权重
    freshnessCount: 20,     // 时效性关键词 - 中权重
    outdatedCount: 15,      // 过时标记 - 负面权重
  };

  // 计算各项得分 (0-100)
  const metaDateFinalScore = Math.min(metaDateScore * 2, 100); // meta日期最高100
  const dateScore = Math.min(dateCount * 10, 100); // 10个时间标记为满分
  const versionScore = Math.min(versionCount * 20, 100); // 5个版本标记为满分
  const freshnessKeywordScore = Math.min(freshnessCount * 10, 100); // 10个时效性关键词为满分
  const outdatedPenalty = Math.min(outdatedCount * 15, 50); // 最多扣50分

  const freshnessScores = {
    metaDateScore: metaDateFinalScore,
    dateCount: dateScore,
    versionCount: versionScore,
    freshnessCount: freshnessKeywordScore,
    outdatedCount: Math.max(0, 100 - outdatedPenalty), // 转换为正向评分
  };

  // 加权计算总分
  let overallFreshness = 0;
  let totalWeight = 0;
  for (const [key, weight] of Object.entries(freshnessWeights)) {
    overallFreshness += freshnessScores[key] * weight;
    totalWeight += weight;
  }
  freshnessScore.overallFreshness = Math.round(overallFreshness / totalWeight);

  if (freshnessScore.overallFreshness >= 80) {
    freshnessScore.recommendation = '内容新鲜度良好，AI搜索偏好新鲜内容';
  } else if (freshnessScore.overallFreshness >= 60) {
    freshnessScore.recommendation = '内容新鲜度中等，建议添加更新日期和时效性信息';
  } else {
    freshnessScore.recommendation = '内容新鲜度较低，建议更新内容并添加时间标记';
  }

  return freshnessScore;
}

