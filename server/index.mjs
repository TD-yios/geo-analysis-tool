  const readabilityDetails = {
    avgWordsPerParagraph: Math.round(avgWordsPerParagraph),
    avgWordsPerSentence: Math.round(avgWordsPerSentence),
    totalWords: words,
    totalSentences: sentences
  };
  
  // 高优先级检测：段落单任务性
  const allParagraphs = $('p').map((i, el) => $(el).text().trim()).get();
