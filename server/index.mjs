function detectPageType(url, $) {
  let urlObj;
  try { urlObj = new URL(url); } catch { return { isHomepage: false, isCategory: false, isArticle: false, isProduct: false, isAbout: false, isContact: false, type: 'other' }; }
  const pathname = urlObj.pathname;

  // 首页检测：路径为空、只有/或常见首页文件名
  const isHomepage = pathname === '' || pathname === '/' ||
    /\/index\.(html|htm|php|aspx?)$/i.test(pathname);

  // 分类页检测
  const isCategory = /category|tag|archive|type/i.test(pathname) ||
    $('meta[name="article:section"]').length > 0 ||
    $('meta[property="article:section"]').length > 0;

  // 文章页检测
  const isArticle = /\/\d{4}\/|\/post\/|\/article\/|\/blog\//i.test(pathname) ||
    $('article').length > 0 ||
    $('meta[property="og:type"]').attr('content') === 'article' ||
    $('meta[name="article:published_time"]').length > 0;

  // 产品页检测
  const isProduct = /product|item|goods/i.test(pathname) ||
    $('meta[property="og:type"]').attr('content') === 'product' ||
    $('meta[property="product:price:amount"]').length > 0;

  // 关于页检测
  const isAbout = /about|about-us|关于/i.test(pathname);

  // 联系页检测
  const isContact = /contact|联系我们/i.test(pathname);

  return {
    isHomepage,
    isCategory,
    isArticle,
    isProduct,
    isAbout,
    isContact,
    type: isHomepage ? 'homepage' :
          isCategory ? 'category' :
          isArticle ? 'article' :
          isProduct ? 'product' :
          isAbout ? 'about' :
          isContact ? 'contact' : 'other'
  };
}

// 检测页面渲染模式
function detectRenderingMode(html, $) {
  const body = $('body').html() || '';
  const head = $('head').html() || '';
  const fullHtml = html.toLowerCase();

  // SPA检测指标
  const hasReactRoot = $('#root, #app, [data-reactroot], [data-reactid]').length > 0;
  const hasVueApp = $('[data-v-app], #app[data-v-], .v-application').length > 0;
  const hasAngularApp = $('app-root, [ng-app], [data-ng-app], [ng-version]').length > 0;
  const hasNextData = $('#__NEXT_DATA__').length > 0 || /__NEXT_DATA__/i.test(html);
  const hasNextAppRouter = /_next\/static\/chunks/i.test(html) || /__next_f/i.test(html);
  const hasNuxtData = $('#__NUXT__, #__nuxt').length > 0 || /__NUXT__/i.test(html);
  const hasRemixData = /__remixManifest|__remixContext/i.test(html);
  const hasGatsby = /___gatsby/i.test(html) || $('meta[name="gatsby-inferred-locale"]').length > 0;
  const hasSvelteKit = $('[data-sveltekit]').length > 0;
  const hasAstroIsland = $('astro-island, astro-slot').length > 0 || /astro-island/i.test(html);

  // JS框架特征
  const hasReactScripts = /react\.production\.min|react-dom/i.test(fullHtml);
  const hasVueScripts = /vue\.global|vue\.runtime/i.test(fullHtml);
  const hasAngularScripts = /angular.*min\.js|@angular/i.test(fullHtml);
  const hasSvelteScripts = /svelte.*internal/i.test(fullHtml);

  // 内容特征判断
  const bodyTextLength = getCleanText($).length;
  const hasSignificantContent = bodyTextLength > 200;
  const scriptCount = $('script').length;
  const hasManyScripts = scriptCount > 5;
  const hasLazyHydration = /hydrate|lazy.*load|dynamic.*import/i.test(fullHtml);

  // SSR特征：有内容 + 有框架标记
  const hasSsrContent = hasSignificantContent && (hasReactRoot || hasVueApp || hasAngularApp || hasNextData || hasNuxtData);

  // ISR特征：有revalidate标记
  const hasIsr = /revalidate|__rewindId|incremental.*static/i.test(fullHtml);

  // 静态特征：少脚本、无框架标记
  const isLikelyStatic = !hasReactRoot && !hasVueApp && !hasAngularApp && !hasNextData && !hasNextAppRouter && !hasNuxtData && !hasRemixData && !hasGatsby && !hasSvelteKit && !hasAstroIsland && scriptCount <= 3;

  let mode = 'static';
  let framework = 'unknown';
  let confidence = 'high';
  const warnings = [];

  if (hasNextData || hasNextAppRouter) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'nextjs';
    if (hasIsr) mode = 'isr';
    confidence = 'high';
  } else if (hasNuxtData) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'nuxt';
    confidence = 'high';
  } else if (hasGatsby) {
    mode = 'ssg';
    framework = 'gatsby';
    confidence = 'high';
  } else if (hasRemixData) {
    mode = 'ssr';
    framework = 'remix';
    confidence = 'high';
  } else if (hasAstroIsland) {
    mode = 'ssg';
    framework = 'astro';
    confidence = 'high';
  } else if (hasSvelteKit) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'sveltekit';
    confidence = 'high';
  } else if (hasAngularApp || hasAngularScripts) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'angular';
    confidence = hasAngularApp ? 'high' : 'medium';
  } else if (hasReactRoot || hasReactScripts) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'react';
    confidence = hasReactRoot ? 'high' : 'medium';
  } else if (hasVueApp || hasVueScripts) {
    mode = hasSsrContent ? 'ssr' : 'spa';
    framework = 'vue';
    confidence = hasVueApp ? 'high' : 'medium';
  } else if (isLikelyStatic) {
    mode = 'static';
    framework = 'plain';
    confidence = 'high';
  } else if (hasManyScripts && hasSignificantContent) {
    // Many scripts + content but no framework markers = traditional server-rendered site
    mode = 'ssr';
    framework = 'traditional';
    confidence = 'medium';
  } else if (hasManyScripts && !hasSignificantContent) {
    mode = 'spa';
    framework = 'unknown';
    confidence = 'low';
  } else {
    // Default: has some scripts and content, likely server-rendered
    mode = 'ssr';
    framework = 'unknown';
    confidence = 'low';
  }

  // SPA内容不足警告
  if (mode === 'spa' && !hasSignificantContent) {
    warnings.push('SPA页面内容不足，可能因JavaScript未执行导致内容缺失。建议使用无头浏览器获取完整内容。');
  }

  // 低置信度警告
  if (confidence === 'low') {
    warnings.push('渲染模式检测置信度较低，结果可能不准确。');
  }

  return {
    mode,
    framework,
    confidence,
    warnings,
    details: {
      hasSignificantContent,
      scriptCount,
      hasReactRoot,
      hasVueApp,
      hasAngularApp,
      hasNextData,
      hasNextAppRouter,
      hasNuxtData,
      hasGatsby,
      hasSvelteKit,
      hasAstroIsland,
      hasIsr,
      hasLazyHydration
    }
  };
}

// 检测页面语言
function detectLanguage(html, $, url) {
  // 1. 先通过内容特征检测（最可靠，因为很多网站lang属性设置错误）
  const text = getCleanText($).toLowerCase();

  // 中文检测 - 降低阈值到10%，且只要有5个以上汉字即判定
  const chineseChars = (text.match(/[\u4e00-\u9fa5]/g) || []).length;
  if (chineseChars > 5 || chineseChars > text.length * 0.1) {
    return 'zh';
  }

  // 日文检测
  const japaneseChars = (text.match(/[\u3040-\u309f\u30a0-\u30ff]/g) || []).length;
  if (japaneseChars > 3 || japaneseChars > text.length * 0.1) {
    return 'ja';
  }

  // 韩文检测
  const koreanChars = (text.match(/[\uac00-\ud7af]/g) || []).length;
  if (koreanChars > 3 || koreanChars > text.length * 0.1) {
    return 'ko';
  }

  // 阿拉伯文检测
  const arabicChars = (text.match(/[\u0600-\u06ff]/g) || []).length;
  if (arabicChars > 3 || arabicChars > text.length * 0.1) {
    return 'ar';
  }

  // 2. 检查html lang属性（回退，因为很多网站设置错误）
  const htmlLang = $('html').attr('lang') || $('html').attr('xml:lang');
  if (htmlLang) {
    const lang = htmlLang.split('-')[0].toLowerCase();
    if (['zh', 'en', 'ja', 'ko', 'es', 'fr', 'de', 'ru', 'ar'].includes(lang)) {
      return lang;
    }
  }

  // 3. 检查meta标签
  const metaLang = $('meta[http-equiv="content-language"]').attr('content');
  if (metaLang) {
    const lang = metaLang.split('-')[0].toLowerCase();
    if (['zh', 'en', 'ja', 'ko', 'es', 'fr', 'de', 'ru', 'ar'].includes(lang)) {
      return lang;
    }
  }

  // 4. 检查charset meta标签
  const charset = $('meta[charset]').attr('charset') ||
    ($('meta[http-equiv="content-type"]').attr('content') || '').match(/charset=([^\s;]+)/i)?.[1];
  if (charset) {
    const cs = charset.toLowerCase();
    if (cs === 'gb2312' || cs === 'gbk' || cs === 'gb18030') return 'zh';
    if (cs === 'euc-jp' || cs === 'shift_jis' || cs === 'iso-2022-jp') return 'ja';
    if (cs === 'euc-kr' || cs === 'ks_c_5601') return 'ko';
  }

  // 5. 域名TLD提示
  if (url) {
    try {
      const hostname = new URL(url).hostname;
      if (/\.cn$|\.com\.cn$|\.net\.cn$/i.test(hostname)) return 'zh';
      if (/\.jp$|\.co\.jp$/i.test(hostname)) return 'ja';
      if (/\.kr$|\.co\.kr$/i.test(hostname)) return 'ko';
      if (/\.ru$/i.test(hostname)) return 'ru';
      if (/\.de$/i.test(hostname)) return 'de';
      if (/\.fr$/i.test(hostname)) return 'fr';
      if (/\.es$/i.test(hostname)) return 'es';
      if (/\.sa$|\.ae$|\.eg$|\.qa$|\.kw$|\.bh$|\.om$/i.test(hostname)) return 'ar';
    } catch(e) {}
  }

  // 默认返回英文
  return 'en';
}

