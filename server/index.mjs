// 检查robots.txt中的AI爬虫配置
async function checkRobotsTxt(baseUrl) {
  try {
    const robotsUrl = new URL('/robots.txt', baseUrl).href;
    const response = await fetchWithRetry(robotsUrl, { timeout: 5000 });

    // fetchWithRetry returns axios response which throws on non-2xx, so success means valid response
    const content = response.data;
    const lines = content.split('\n');

    const crawlerStatus = AI_CRAWLERS.map(crawler => {
      let status = 'allowed';
      let recommendation = '允许AI爬虫访问';

      for (let lineIdx = 0; lineIdx < lines.length; lineIdx++) {
        const trimmedLine = lines[lineIdx].trim().toLowerCase();
        if (trimmedLine.startsWith('user-agent:')) {
          const userAgent = trimmedLine.replace('user-agent:', '').trim();
          if (userAgent === crawler.userAgent.toLowerCase() || userAgent === '*') {
            // 检查后续的Disallow/Allow规则
            for (let i = lineIdx + 1; i < lines.length; i++) {
              const nextLine = lines[i].trim().toLowerCase();
              if (nextLine.startsWith('user-agent:')) {
                break;
              }
              if (nextLine.startsWith('disallow:')) {
                const disallowPath = nextLine.replace('disallow:', '').trim();
                if (disallowPath === '/') {
                  status = 'blocked';
                  recommendation = `阻止${crawler.name}访问整个站点`;
                }
                // Disallow: (空) 意味着允许所有，不改变status
              }
              if (nextLine.startsWith('allow:')) {
                const allowPath = nextLine.replace('allow:', '').trim();
                if (allowPath === '/' && status === 'blocked') {
                  // Allow: / 覆盖之前的 Disallow: /
                  status = 'allowed';
                  recommendation = '允许AI爬虫访问';
                }
              }
            }
          }
        }
      }

      return {
        ...crawler,
        status,
        recommendation
      };
    });

    return {
      exists: true,
      content,
      aiCrawlers: crawlerStatus
    };
  } catch (error) {
    return {
      exists: false,
      content: null,
      aiCrawlers: AI_CRAWLERS.map(crawler => ({
        ...crawler,
        status: 'unknown',
        recommendation: '无法检测robots.txt'
      }))
    };
  }
}

