// 内容可引用性评分
function analyzeCitability(html, $, pageType) {
  const bodyText = getCleanText($);

  // 段落提取 - 多重回退策略
  let citParagraphs = $('p').map((i, el) => $(el).text().trim()).get().filter(p => p.length > 10);
  // 回退: SPA/框架可能用div代替p
  if (citParagraphs.length < 3) {
    const altParagraphs = $('article p, main p, [role="main"] p, .content p, .post-content p, .entry-content p').map((i, el) => $(el).text().trim()).get().filter(p => p.length > 10);
    if (altParagraphs.length > citParagraphs.length) citParagraphs = altParagraphs;
  }
  if (citParagraphs.length < 3) {
    const divParagraphs = $('article > div, main > div, [role="main"] > div').map((i, el) => {
      const text = $(el).text().trim();
      return text.length > 20 && text.length < 1000 ? text : '';
    }).get().filter(p => p);
    if (divParagraphs.length > citParagraphs.length) citParagraphs = divParagraphs;
  }

  // 分析段落长度
  const paragraphLengths = citParagraphs.map(p => p.length);
  const avgParagraphLength = paragraphLengths.length > 0
    ? paragraphLengths.reduce((a, b) => a + b, 0) / paragraphLengths.length
    : 0;

  // 最佳段落长度比例（40-200字，适合中文和英文）
  const optimalLengthCount = paragraphLengths.filter(len => len >= 40 && len <= 200).length;
  const optimalLengthRatio = paragraphLengths.length > 0 ? optimalLengthCount / paragraphLengths.length : 0;

  // 检测自包含内容 - 支持中英文
  const hasSelfContainedContent = citParagraphs.some(p => {
    const cnChars = (p.match(/[\u4e00-\u9fa5]/g) || []).length;
    const enWords = p.split(/\s+/).filter(w => w.length > 2).length;
    return (cnChars >= 50 && cnChars <= 300) || (enWords >= 30 && enWords <= 150);
  });

  // 事实密度检测 - 多格式（数字、日期、货币、度量等）
  const factPatterns = /\d+(\.\d+)?%|\d+万|\d+亿|\d+k|\d+m|\$\d+|€\d+|¥\d+|\d{4}年|\d{1,2}月|\d{1,2}日|\d{4}-\d{2}-\d{2}|据.*统计|数据显示|研究表明|according to|research shows|statistics indicate|survey found|\d+\s*(million|billion|thousand|percent|万|亿)/gi;
  const factCount = (bodyText.match(factPatterns) || []).length;
  const factDensity = bodyText.length > 0 ? factCount / (bodyText.length / 1000) : 0;

  // 问题导向检测 - 多语言
  const hasQuestionInContent = /如何|怎么|为什么|什么是|怎样|哪些|哪个|多少|是否|how|why|what|where|when|which|who|can I|is there|does it/gi.test(bodyText);
  const hasAnswerPattern = /因为|由于|可以通过|建议|应该|原因是|解决方案|because|therefore|solution|recommend|the answer|you can|should|it is recommended/gi.test(bodyText);
  const hasQuestionAnswer = hasQuestionInContent && hasAnswerPattern;

  // 清晰度评分 - 多重检测
  const hasClearStructure = $('h1, h2, h3').length > 0;
  const hasListItems = $('ul, ol, dl').length > 0;
  const hasTableData = $('table').length > 0;

  const citabilityScore = {
    avgParagraphLength: Math.round(avgParagraphLength),
    optimalLengthRatio: Math.round(optimalLengthRatio * 100),
    hasSelfContainedContent,
    factDensity: Math.round(factDensity * 10) / 10,
    hasQuestionAnswer,
    hasClearStructure: hasClearStructure || hasListItems || hasTableData,
    overallScore: 0
  };

  // 计算综合可引用性评分 - 使用加权算法
  const citabilityWeights = {
    optimalLengthRatio: 25,
    hasSelfContainedContent: 25,
    factDensity: 25,
    hasQuestionAnswer: 15,
    hasClearStructure: 10,
  };

  const citabilityScores = {
    optimalLengthRatio: optimalLengthRatio * 100,
    hasSelfContainedContent: hasSelfContainedContent ? 100 : 0,
    factDensity: Math.min(factDensity * 50, 100),
    hasQuestionAnswer: hasQuestionAnswer ? 100 : 0,
    hasClearStructure: (hasClearStructure && hasListItems) ? 100 : (hasClearStructure || hasTableData) ? 60 : 0,
  };

  let overallScore = 0;
  let totalWeight = 0;
  for (const [key, weight] of Object.entries(citabilityWeights)) {
    overallScore += citabilityScores[key] * weight;
    totalWeight += weight;
  }
  citabilityScore.overallScore = Math.round(overallScore / totalWeight);

  return citabilityScore;
}

// Schema生成器
function generateSchema(result) {
  const { url, details, pageType } = result;
  let hostname;
  try { hostname = new URL(url).hostname; } catch { hostname = url; }

  const schemas = [];

  // Organization Schema
  schemas.push({
    '@context': 'https://schema.org',
    '@type': 'Organization',
    name: hostname,
    url: url,
    sameAs: []
  });

  // Article Schema (如果是文章页面)
  if (pageType?.isArticle) {
    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'Article',
      headline: details.contentStructure?.titleOptimization?.title || hostname || 'Article',
      author: details.eeat?.hasAuthorInfo ? {
        '@type': 'Person',
        name: 'Author'
      } : undefined,
      datePublished: new Date(result.timestamp).toISOString(),
      dateModified: new Date(result.timestamp).toISOString(),
      mainEntityOfPage: {
        '@type': 'WebPage',
        '@id': url
      }
    });
  }

  // WebSite Schema
  schemas.push({
    '@context': 'https://schema.org',
    '@type': 'WebSite',
    name: hostname,
    url: url,
    potentialAction: {
      '@type': 'SearchAction',
      target: `${url}/search?q={search_term_string}`,
      'query-input': 'required name=search_term_string'
    }
  });

  // FAQ Schema (如果有FAQ内容)
  if (details.contentStructure?.hasIntroduction) {
    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'FAQPage',
      mainEntity: []
    });
  }

  return schemas.filter(s => s !== undefined);
}

