function generateRecommendations(details, dimensions, pageType = { type: 'other' }, language = 'en') {
  const recommendations = [];

  dimensions.forEach(dim => {
    dim.items.forEach(item => {
      if (item.recommendation) {
        // 根据页面类型调整业务影响和难度评分
        let businessImpact = 50;
        let difficulty = 50;
        let roi = 50;

        // 首页的优化通常业务影响更大
        if (pageType.isHomepage) {
          businessImpact += 20;
        }

        // 文章页的内容优化ROI较高
        if (pageType.isArticle) {
          roi += 15;
          businessImpact += 10;
        }

        // 产品页的E-E-A-T优化业务影响大
        if (pageType.isProduct) {
          businessImpact += 15;
          difficulty += 10;
        }

        // 技术类建议通常难度较高但ROI也高
        if (dim.name === '技术基础') {
          difficulty += 20;
          roi += 20;
        }

        // 内容类建议难度较低
        if (dim.name === '内容结构') {
          difficulty -= 10;
        }

        // 根据状态调整评分
        if (item.status === 'fail') {
          businessImpact += 20;
          roi += 15;
        } else if (item.status === 'warning') {
          businessImpact += 10;
          roi += 5;
        }

        // 确保评分在0-100范围内
        businessImpact = Math.min(100, Math.max(0, businessImpact));
        difficulty = Math.min(100, Math.max(0, difficulty));
        roi = Math.min(100, Math.max(0, roi));

        recommendations.push({
          priority: item.status === 'fail' ? 'high' : item.status === 'warning' ? 'medium' : 'low',
          category: dim.name,
          title: item.name,
          description: item.description,
          action: item.recommendation,
          businessImpact,
          difficulty,
          roi
        });
      }
    });
  });

  // 智能排序：优先级 > ROI > 业务影响 > 难度（难度越低越好）
  return recommendations.sort((a, b) => {
    const priorityOrder = { high: 0, medium: 1, low: 2 };
    const priorityDiff = priorityOrder[a.priority] - priorityOrder[b.priority];
    if (priorityDiff !== 0) return priorityDiff;

    // 相同优先级时，按ROI降序
    if (b.roi !== a.roi) return b.roi - a.roi;

    // ROI相同时，按业务影响降序
    if (b.businessImpact !== a.businessImpact) return b.businessImpact - a.businessImpact;

    // 业务影响相同时，按难度升序（越容易越好）
    return a.difficulty - b.difficulty;
  });
}

