// 提取干净的页面文本（去除script/style/noscript/svg污染）
function getCleanText($) {
  // 优先从内容区域提取
  const contentSelectors = 'article, main, [role="main"], .content, .post, .entry, section, .page-content';
  const contentElements = $(contentSelectors);
  if (contentElements.length > 0) {
    const clone = contentElements.clone();
    clone.find('script, style, noscript, svg, pre, code').remove();
    const text = clone.text().trim();
    if (text.length > 50) return text;
  }
  // 回退：从body提取但移除script/style/code
  const bodyClone = $('body').clone();
  bodyClone.find('script, style, noscript, svg, pre, code').remove();
  return bodyClone.text() || $.text() || '';
}
