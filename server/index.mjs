// 全站分析函数（基于URL深度）
async function analyzeFullSite(baseUrl, maxDepth = 2, progressCallback = null) {
  const startTime = Date.now();
  const visitedUrls = new Set();
  const results = [];
  const urlQueue = [{ url: baseUrl, depth: 0 }];
  let processedCount = 0;
  let discoveredCount = 1; // 已发现的总页面数（包括队列中的）
  
  // 提取域名用于同源检查
  const baseDomain = new URL(baseUrl).hostname;
  
  while (urlQueue.length > 0) {
    const { url, depth } = urlQueue.shift();
    
    // 规范化URL：统一为https，移除尾部斜杠，避免HTTP/HTTPS重复
    const normalizedUrl = url.replace(/^http:/, 'https:').replace(/\/+$/, '') || url;
    // 跳过已访问的URL
    if (visitedUrls.has(normalizedUrl)) continue;
    visitedUrls.add(normalizedUrl);
    
    // 深度控制
    if (depth > maxDepth) continue;
    
    try {
      console.log(`Analyzing: ${url} (depth: ${depth})`);
      
      // 发送进度更新
      if (progressCallback) {
        processedCount++;
        const progress = {
          currentUrl: url,
          depth,
          processed: processedCount,
          total: discoveredCount,
          estimated: discoveredCount,
          percentage: Math.min(100, Math.round((processedCount / discoveredCount) * 100))
        };
        progressCallback(progress);
      }
      
      // 分析当前页面
      const pageAnalysis = await analyzeWebsite(url);
      results.push(pageAnalysis);
      
      // 如果未达到最大深度，爬取更多链接
      if (depth < maxDepth) {
        const links = await extractLinks(url, baseDomain);
        for (const link of links) {
          const normalizedLink = link.replace(/^http:/, 'https:').replace(/\/+$/, '') || link;
          if (!visitedUrls.has(normalizedLink)) {
            urlQueue.push({ url: link, depth: depth + 1 });
            discoveredCount++;
          }
        }
      }
    } catch (error) {
      console.error(`Failed to analyze ${url}:`, error.message);
      // 继续处理其他URL
    }
  }
  
  console.log(`Analyzed ${results.length} pages in ${Date.now() - startTime}ms`);
  
  // 汇总结果
  return aggregateResults(results, baseUrl);
}

// 提取页面中的同源链接
async function extractLinks(url, baseDomain) {
  try {
    const response = await fetchWithRetry(url, { timeout: 5000 });
    const $ = cheerio.load(response.data);
    const links = new Set();
    
    $('a[href]').each((i, el) => {
      let href = $(el).attr('href');
      if (!href) return;
      
      // 转换为绝对URL
      try {
        const absoluteUrl = new URL(href, url).href;
        const urlObj = new URL(absoluteUrl);
        
        // 只爬取同域名的链接
        if (urlObj.hostname === baseDomain) {
          // 移除fragment和query参数
          urlObj.hash = '';
          urlObj.search = '';
          const cleanUrl = urlObj.href;
          
          // 排除静态资源
          const excludeExtensions = ['.css', '.js', '.png', '.jpg', '.jpeg', '.gif', '.svg', '.ico', '.pdf', '.zip', '.xml'];
          const hasExcludedExt = excludeExtensions.some(ext => cleanUrl.toLowerCase().endsWith(ext));
          
          if (!hasExcludedExt) {
            links.add(cleanUrl);
          }
        }
      } catch {
        // 忽略无效URL
      }
    });
    
    return Array.from(links);
  } catch (error) {
    console.error(`Failed to extract links from ${url}:`, error.message);
    return [];
  }
}

// 汇总多个页面的分析结果
function aggregateResults(results, baseUrl) {
  if (results.length === 0) {
    return getFallbackAnalysis(baseUrl);
  }
  
  // 汇总各维度分数
  const aggregatedDimensions = {
    technical: { score: 0, items: [] },
    structuredData: { score: 0, items: [] },
    contentStructure: { score: 0, items: [] },
    eeat: { score: 0, items: [] },
    aiCitation: { score: 0, items: [] }
  };
  
  results.forEach(result => {
    result.dimensions.forEach(dim => {
      const dimKey = {
        '技术基础': 'technical',
        '结构化数据': 'structuredData',
        '内容结构': 'contentStructure',
        'E-E-A-T 信号': 'eeat',
        'AI 引用模拟': 'aiCitation'
      }[dim.name];
      
      if (dimKey && aggregatedDimensions[dimKey]) {
        aggregatedDimensions[dimKey].score += dim.score;
        aggregatedDimensions[dimKey].items.push(...(dim.items || []));
      }
    });
  });
  
  // 计算平均分
  const finalDimensions = Object.entries(aggregatedDimensions).map(([key, value]) => {
    const avgScore = Math.round(value.score / results.length);
    return {
      name: {
        technical: '技术基础',
        structuredData: '结构化数据',
        contentStructure: '内容结构',
        eeat: 'E-E-A-T 信号',
        aiCitation: 'AI 引用模拟'
      }[key],
      score: avgScore,
      weight: key === 'technical' ? 0.2 : key === 'structuredData' ? 0.2 : key === 'contentStructure' ? 0.25 : key === 'eeat' ? 0.15 : 0.2,
      items: value.items
    };
  });
  
  // 计算总分
  const overallScore = finalDimensions.reduce((sum, dim) => sum + dim.score * dim.weight, 0);
  
  // 生成智能排序的建议
  const recommendations = [];
  finalDimensions.forEach(dim => {
    (dim.items || []).forEach((item) => {
      if (item.status !== 'pass') {
        // 计算业务影响分数
        const businessImpact = calculateBusinessImpact(item, dim.name, overallScore);
        
        recommendations.push({
          priority: item.status === 'fail' ? 'high' : 'medium',
          category: dim.name,
          title: item.name,
          description: item.description,
          action: item.recommendation || `优化${item.name}以提升${dim.name}评分`,
          businessImpact: businessImpact.score,
          difficulty: businessImpact.difficulty,
          roi: businessImpact.roi,
          affectedPages: item.affectedPages || results.length
        });
      }
    });
  });
  
  // 智能排序：优先级 > 业务影响 > ROI > 难度
  const priorityWeight = { high: 100, medium: 50, low: 0 };
  recommendations.sort((a, b) => {
    const priorityDiff = priorityWeight[b.priority] - priorityWeight[a.priority];
    if (priorityDiff !== 0) return priorityDiff;
    
    const impactDiff = b.businessImpact - a.businessImpact;
    if (Math.abs(impactDiff) > 10) return impactDiff;
    
    const roiDiff = b.roi - a.roi;
    if (Math.abs(roiDiff) > 5) return roiDiff;
    
    return a.difficulty - b.difficulty;
  });
  
  // 按页面类型自动归类
  const pageCategories = {};
  const typeLabels = {
    homepage: '首页', article: '文章页', category: '分类页', product: '产品页',
    about: '关于页', contact: '联系页', blog: '博客页', faq: 'FAQ页',
    documentation: '文档页', landing: '落地页', search: '搜索页',
    tool: '工具页', pricing: '定价页', other: '其他页面'
  };
  results.forEach(result => {
    const type = result.pageType?.type || 'other';
    if (!pageCategories[type]) {
      pageCategories[type] = {
        label: typeLabels[type] || type,
        count: 0,
        totalScore: 0,
        urls: []
      };
    }
    pageCategories[type].count++;
    pageCategories[type].totalScore += result.overallScore || 0;
    pageCategories[type].urls.push(result.url);
  });
  // 计算各类平均分并排序
  const pageCategoriesList = Object.entries(pageCategories)
    .map(([type, data]) => ({
      type,
      label: data.label,
      count: data.count,
      avgScore: Math.round(data.totalScore / data.count),
      urls: data.urls
    }))
    .sort((a, b) => b.count - a.count);

  return {
    url: baseUrl,
    timestamp: Date.now(),
    overallScore: Math.round(overallScore),
    dimensions: finalDimensions,
    details: {
      // 合并首页的详细分析数据，使前端能正常渲染各详情面板
      ...(results[0]?.details || {}),
      totalPages: results.length,
      analyzedUrls: results.map(r => r.url)
    },
    pageCategories: pageCategoriesList,
    recommendations: recommendations.slice(0, 20), // 限制建议数量
    analysisTime: Date.now() - (results[0]?.timestamp ? results[0].timestamp - (results[0].analysisTime || 0) : Date.now()),
    pageType: results[0]?.pageType,
    language: results[0]?.language,
    renderingMode: results[0]?.renderingMode,
    mode: 'full_site'
  };
}

// 计算业务影响分数
function calculateBusinessImpact(item, dimension, overallScore) {
  let impactScore = 50;
  let difficulty = 50;
  let roi = 50;
  
  // 基于维度的影响权重
  const dimensionWeights = {
    '技术基础': { impact: 90, difficulty: 30, roi: 95 },
    '结构化数据': { impact: 85, difficulty: 40, roi: 90 },
    '内容结构': { impact: 70, difficulty: 60, roi: 80 },
    'E-E-A-T 信号': { impact: 80, difficulty: 70, roi: 85 },
    'AI 引用模拟': { impact: 75, difficulty: 50, roi: 88 }
  };
  
  const dimWeight = dimensionWeights[dimension] || { impact: 50, difficulty: 50, roi: 50 };
  
  // 基于项目状态的影响
  if (item.status === 'fail') {
    impactScore += 20;
    roi += 15;
  } else if (item.status === 'warning') {
    impactScore += 10;
    roi += 5;
  }
  
  // 基于项目类型的影响
  const highImpactItems = ['robots.txt', 'HTTPS', 'Schema', 'llms.txt', 'sitemap'];
  const mediumImpactItems = ['性能', '移动端', '标题', 'Meta标签'];
  
  if (highImpactItems.some(keyword => item.name.includes(keyword))) {
    impactScore += 15;
    roi += 20;
    difficulty -= 10;
  } else if (mediumImpactItems.some(keyword => item.name.includes(keyword))) {
    impactScore += 8;
    roi += 10;
  }
  
  // 基于当前整体评分的影响（分数越低，修复价值越高）
  if (overallScore < 50) {
    roi += 15;
    impactScore += 10;
  } else if (overallScore < 70) {
    roi += 10;
  }
  
  // 某些项目实施难度较高
  const highDifficultyItems = ['内容结构', 'E-E-A-T', '案例', '方法论'];
  if (highDifficultyItems.some(keyword => item.name.includes(keyword))) {
    difficulty += 20;
    roi -= 5;
  }
  
  // 确保分数在0-100范围内
  impactScore = Math.max(0, Math.min(100, impactScore));
  difficulty = Math.max(0, Math.min(100, difficulty));
  roi = Math.max(0, Math.min(100, roi));
  
  return {
    score: Math.round(impactScore),
    difficulty: Math.round(difficulty),
    roi: Math.round(roi)
  };
}

