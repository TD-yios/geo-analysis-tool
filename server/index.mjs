  const titleCoverageScore = keywords.length > 0 ? Math.min(titleKeywords.length / keywords.length * 100, 100) : 0;
  const metaCoverageScore = keywords.length > 0 ? Math.min(metaKeywords.length / keywords.length * 100, 100) : 0;
  const keywordCountScore = Math.min(keywords.length * 5, 100); // 20个关键词为满分
